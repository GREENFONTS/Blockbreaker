import game from './game.mjs';
import bricks from '../src/bricks.mjs';


 export function buildLevel(Game, level) {
    let Brick = [];

    level.forEach((row, rowIndex) => {
        row.forEach((brick, brickIndex) => {
            if (brick === 1) {
                let position = {
                    x: 80 * brickIndex,
                    y: 70 + 23 * rowIndex
                };
                Brick.push(new bricks(Game, position))
            };
        });
    }
     );
     return Brick;
}

